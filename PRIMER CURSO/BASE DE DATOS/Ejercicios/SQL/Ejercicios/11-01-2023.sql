SELECT DETALLE.*, PRODUCTO.DESCRIPCION
FROM DETALLE INNER JOIN PRODUCTO
ON DETALLE.PRODUCTO_NUMERO = PRODUCTO.PRODUCTO_NUMERO
ORDER BY PRODUCTO_NUMERO;