-- LISTAR: nombre y cargo de la tabla EMPLEADO y DEPARTAMENTO al que pertenece.

SELECT EMPLEADO.NOMBRE, EMPLEADO.CARGO, DEPARTAMENTO.DNOM AS DEPARTAMENTO
FROM EMPLEADO INNER JOIN DEPARTAMENTO
ON EMPLEADO.DEPT_NO = DEPARTAMENTO.DEPT_NO
ORDER BY DNOM ASC;

