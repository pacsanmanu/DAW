package Ej7.Clases;

public class Camiseta {
    public String Color;
    public char Talla;
    public String Marca;
    public String Tejido;

    public Camiseta(String Color, char Talla, String Marca, String Tejido){
        this.Color = Color;
        this.Talla = Talla;
        this.Marca = Marca;
        this.Tejido = Tejido;
    }
}

